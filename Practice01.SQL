-- 문제 1.
SELECT FIRST_NAME||' '||LAST_NAME 이름, SALARY 월급, PHONE_NUMBER 전화번호,
       HIRE_DATE 입사일
FROM EMPLOYEES
ORDER BY HIRE_DATE;

-- 문제 2.
SELECT JOB_TITLE, MAX_SALARY
FROM JOBS
ORDER BY MAX_SALARY DESC;

-- 문제 3.
SELECT FIRST_NAME, MANAGER_ID, COMMISSION_PCT,SALARY
FROM EMPLOYEES
WHERE COMMISSION_PCT IS NULL AND MANAGER_ID IS NOT NULL
AND SALARY>3000;

-- 문제 4.
SELECT JOB_TITLE, MAX_SALARY
FROM JOBS
WHERE MAX_SALARY>=10000
ORDER BY MAX_SALARY DESC;

-- 문제 5.
SELECT FIRST_NAME, SALARY, NVL(COMMISSION_PCT,0) COMMISSION_PCT
FROM EMPLOYEES
WHERE SALARY>=10000 AND SALARY <14000
ORDER BY SALARY DESC;

-- 문제 6.
SELECT FIRST_NAME, SALARY, TO_CHAR(HIRE_DATE,'YYYY-MM') HIRE_DATE,DEPARTMENT_ID
FROM EMPLOYEES
WHERE DEPARTMENT_ID IN(10,90,100);

-- 문제 7.
SELECT FIRST_NAME, SALARY
FROM EMPLOYEES
WHERE FIRST_NAME LIKE '%S%' OR LOWER(FIRST_NAME) LIKE '%s%';

-- 문제 8.
SELECT *
FROM DEPARTMENTS
ORDER BY LENGTH(DEPARTMENT_NAME) DESC;

-- 문제 9.
SELECT *
FROM COUNTRIES;

-- 문제 10.
SELECT FIRST_NAME, SALARY, SUBSTR(PHONE_NUMBER,3,3)||'-'||SUBSTR(PHONE_NUMBER,7,3)||'-'||SUBSTR(PHONE_NUMBER,11) PHONE_NUMBER,
        HIRE_DATE
FROM EMPLOYEES
WHERE HIRE_DATE<'13/12/31';




